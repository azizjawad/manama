                                                <h3>Pineapple Mojito</h3>
                                                <p>The Mojito is a fantastic cocktail and it's a very simple recipe to play around with.</p>
                                                <p>The Pineapple Mojito is exactly as it sounds and adds both fresh and juiced pineapple
                                                    to the classic rum, mint, and lime drink. It is an easy variation that you can serve as an option to the Mojito.</p>
