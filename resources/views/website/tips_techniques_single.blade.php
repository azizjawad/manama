                                                </div>
                                            </div>
                                            <div class="col-md-6">
                                                <h3>Strawberry Berryoska</h3>
                                                <p>How about a strawberry lemonade that is for adults only? This quick recipe is the perfect way to make that happen.</p>
                                                <p>The Strawberry Berryoska is incredibly easy. All you need is a great vodka, some sliced strawberries, and fresh-squeezed lemonade.
                                                    It's fun, fruity, and the ultimate in refreshing summer cocktails.</p>
                                            </div>

                                            <div class="col-md-6">
                                                <div class="entry-thumbnail my-4">
                                                    <img src="images/tipstechniques/banana-daquiri.jpg" alt="Post Thumbnail">
