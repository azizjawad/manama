                                            <div class="col-md-6">
                                                <h3>Crimson Crush</h3>
                                                <p>Pink grapefruit, pomegranate seeds, and an elderflower liqueur are the perfect (and easy) complement to a
                                                    great vodka and that is all that you need to create this stunning cocktail.</p>
                                                <p>It is fresh fruit at its best and sipping on a Crimson Crush on a steamy summer day is one of the great joys in life.</p>
                                            </div>

                                            <div class="col-md-6">
                                                <div class="entry-thumbnail my-4">
