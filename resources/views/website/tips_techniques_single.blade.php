                                    <div class="entry-content">
                                        <p>You will often hear the advice that fresh is best when it comes to mixing drinks and it is very true.
                                            Whenever possible, you should be using fresh fruits and juices in order to get the best cocktails possible.
                                            While there are some drinks that use just an accent of fruit, there are some that rely on them completely and those below are some of our favorites.</p>
                                        <p>Among this list, you will find a couple of favorite drink recipes, those that we have all enjoyed for years,
                                            as well as a couple of new takes on popular drinks. Primarily, however, these drinks are all new creations,
                                            developed by talented bartenders and chefs to showcase fresh fruits of the season and they are a lot of fun
                                            to explore.</p>
                                        <p>One note on fresh fruit drinks -- many times the fruit remains in the serving glass, such as the Caipirinha and Mojito.
                                            However, when you want a clean, pulp and seed free drink it is best to double-strain the drink to ensure that you've removed any fruit pieces.</p>
