                                            <div class="col-md-6">
                                                <h3>Frozen Banana Daiquiri</h3>
                                                <p>A favorite from the blender, this is one of the more popular daiquiri recipes out there.</p>
                                                <p>The mix is an easy one: rum, triple sec, lime, sugar, ice, and a fresh banana.
                                                    When blended properly, the Banana Daiquiri is a delight to sip on those hottest days of the year.</p>
                                            </div>

                                            <div class="col-md-6">
