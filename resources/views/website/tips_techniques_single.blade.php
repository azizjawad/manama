                                                <h3>White Peach Julep</h3>
                                                <p>During peach season the markets are filled with a great variety of the popular fruit and this cocktail celebrates the white peach in particular.</p>
                                                <p>It comes from Chef Laurent Tourondel, who has taken the classic Mint Julep and added fresh white peaches and peach liqueur to the bourbon and mint mix.
                                                    The result is a fantastic option for barbecues and pool parties.</p>
