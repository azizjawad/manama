                                                <h3>Blackberry Malt</h3>
                                                <p>This cocktail is a little darker than the rest, but it makes a perfect summer cocktail just the same.</p>
                                                <p>The Blackberry Malt mixes a single malt Irish whiskey with fresh blackberries, lime, and syrup, topping it all off with sparkling water.
                                                    It is refreshing and a great way to enjoy your whiskey all summer long.</p>
