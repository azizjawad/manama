                                            <div class="col-md-6">
                                                <h3>The Lemon, Lime and Blue Caipirinha</h3>
                                                <p>Looking for a fun twist on the popular Caipirinha?</p>
                                                <p>The original recipe uses fresh lime, but this one adds lemon and blueberries to the muddling process,
                                                    giving it a nice fruit kick that is perfect when blueberries are at their peak.</p>
                                            </div>

                                            <div class="col-md-6">
