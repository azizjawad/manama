                                            <div class="col-md-6">
                                                <h3>Watermelon Cucumber Cooler</h3>
                                                <p>Summer would not be summer without watermelon and cucumbers and the two come together in one fabulous drink.</p>
                                                <p>The Watermelon Cucumber Cooler is the epitome of fresh fruit cocktails. It begins with fresh-pressed watermelon juice and a muddle of cucumber.
                                                    To this, the lovely Aviation Gin is added along with soda to make it nice and refreshing.</p>
                                            </div>

