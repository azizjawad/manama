                                            <div class="col-md-6">
                                                <h3>Summertime Fruit Punch Lemonade</h3>
                                                <p>There are many fruits that could go into a fruit punch, this recipe just happens to choose one of the best fruits of the summer.</p>
                                                <p>This punch mixes vodka with watermelon and pink lemonade and it adds a little bit of parsley for color and a little flavor kick
                                                    that is both unusual and delightful.</p>
